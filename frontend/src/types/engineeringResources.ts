export interface EngineeringRecipe {
  id: string
  name: string
  levelRequired: number
  xpReward: number
  icon?: string
  image?: string // Image path for the recipe (e.g., '/images/engineering/dagger.png')
  description: string
  ingredients: {
    resourceId: string // This will be the placeholder metal ID
    amount: number
  }[]
  time: number // Time in seconds to craft
  value: number // Sell value of the crafted item
  // Equipment stats (only for equippable items)
  equipmentStats?: {
    attackType: 'bash' | 'cut' | 'block' | 'stab' // Preferred attack type for this item
    attackScale: number // Multiplier for XP when using preferred attack type (e.g., 1.5x)
    damage?: number // Attack damage (for weapons)
    armor?: number // Armor value (for armor pieces)
    armorStyle?: 'melee' | 'ranged' | 'magic' | 'hybrid' // Armor style class (determines affinity)
    accuracy?: number // Accuracy rating (a in hit chance formula)
    critChance?: number // Base crit chance bonus
    armorType?: 'plate' | 'leather' | 'cloth' | 'hybrid' // Determines mitigation behaviour
    damageReductionPercent?: number // Percent damage reduction provided by the armor
    accuracyPercent?: number // Percent accuracy bonus (for accessories like rings)
  }
}

